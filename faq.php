		<h4>Q: How are students placed in a class?</h4>
				<p>A: Student (except for those enrolled in Math Olympiad Prep) are placed based on their diagnostic test scores.</p>

		<h4>Q: My child is doing very well in school. Why is a diagnostic test required?</h4>
				<p>A: The diagnostic helps us evaluate the learning needs of each student, so that we can place him/her in the appropriate class among peers whom with together they can learn from and motivate each other.</p>

		<h4>Q: How do students advance to the next level class?</h4>
				<p>A: Students must score at least an average of 50% on all homework assigned and all quizzes and tests taken in their current class. They must also take a diagnostic test for the next level and score above 30%.</p>

		<h4>Q: How often are the classes?</h4>
				<p>A: Classes are held once/week during the school year and twice/week during summer.</p>

		<h4>Q: How much time should students spend on homework?</h4>
				<p>A: To achieve optimal results, students in elementary level classes should expect to spend about two hours/week and students in middle school classes about three hours/week. Students in the AMC 8/MATHCOUNTS Accelerated class must commit at least three hours/week (to enroll in the Accelerated class, students must score above 65% on the diagnostic test and be able to meet this time commitment).</p>

		<h4>Q: How is the homework assigned?</h4>
				<p>A: We use a custom web application to assign and distribute most of our homework. Students must submit their answers through the application before the due date.</p>

		<h4>Q: Should parents help their student(s) with homework?</h4>
				<p>A: It is best to let the student(s) solve the problems on their own. We will be explaining and reviewing the questions in class. That said, we suggest parents take an active role in ensuring their student(s) completes the homework and attends the classes each week. Students are not allowed to attend classes if they miss two consecutive homework assignments.</p>
		<h4>Q: Does Nuptse offer classes on Saturdays/Sundays?</h4>
				<p>A: We do not offer classes on the weekends at this time.</p>

		<h4>Q: Why do high school students, instead of adults, teach the classes?</h4>
				<p>A: High school students will connect easily with the students and create a fun learning atmosphere. All high school student coaches have several years of experience in competition math. They are carefully selected based on their performance in MATHCOUNTS, AMC 8, and AMC 10 and their dedication to the Foundation and its vision. All curriculum, homework, and materials are evaluated by teachers and adults prior to implementation.</p>

